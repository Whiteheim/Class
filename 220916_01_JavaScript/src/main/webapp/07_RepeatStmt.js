// 7. 반복문 (Java와 동일)
// 동일한 작업을 여러번 반복할 때 사용

// 1 ~ 10 까지 출력

// for
// while
// do-while

for (let i = 1;  i <= 10;  i++){
	document.write(i);
};

document.write("<br>");

let i1 = 1;
while (i1 < 11){
	document.write(i1);
	i1++;
}

document.write("<br>");

let i2 = 1;
do {
	document.write(i2);
	i2++
} while(i2 <= 10);

document.write("<br>--------------------------<br>");

